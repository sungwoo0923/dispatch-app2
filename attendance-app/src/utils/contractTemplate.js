import { formatDate } from "./dateUtils";

// 식품생산공장 도급/사내협력 근로자를 기준으로 한 표준근로계약서 본문.
// 회사별 실제 근로기준법 검토 없이 사용할 수 없으므로, 현장에서 그대로 쓰기보다는
// 초안(placeholder) 성격으로 제공한다.
export function buildDefaultContract({
  employeeName,
  hireDate,
  position,
  siteName,
  vendorName,
  companyName,
  payType,
  shiftType,
  employmentType,
}) {
  return `표 준 근 로 계 약 서
(식품생산공장 · 도급직원용)

${companyName || "회사"}(이하 "갑"이라 한다)과(와) ${employeeName || "근로자"}(이하 "을"이라 한다)은 다음과 같이 근로계약을 체결하고, 이를 성실히 이행할 것을 약정한다.

제1조 (근로계약기간)
${hireDate ? formatDate(hireDate) : "-"} 부터 별도 정함이 없는 한 계속 근로하는 것으로 한다. 단, 계약직인 경우 별도 계약서에서 정한 기간에 따른다.

제2조 (근무장소 및 업무내용)
1. 근무장소: ${siteName || "회사가 지정하는 생산공장(센터)"}
2. 소속업체: ${vendorName || "-"}
3. 업무내용: 식품 생산라인 제조·가공·포장·품질관리 및 이에 부수하는 업무, 직급: ${position || "-"}

제3조 (소정근로시간 및 근무형태)
1. 근무형태: ${employmentType || "-"} / 근무구분: ${shiftType || "-"}
2. 소정근로시간은 회사가 지정하는 근무시간템플릿(시업·종업시각, 휴게시간) 및 스케줄에 따르며, 1일 8시간, 1주 40시간을 원칙으로 한다.
3. 휴게시간은 근로기준법 제54조에 따라 근로시간 4시간마다 30분 이상, 8시간 근무 시 1시간 이상 부여한다.

제4조 (근무일 및 휴일)
1. 근무일은 회사 스케줄에 따르며, 주휴일은 1주 소정근로일을 개근한 경우 유급으로 부여한다.
2. 근로자의 날, 관공서의 공휴일에 관한 규정에 따른 공휴일은 유급휴일로 한다.

제5조 (임금)
1. 지급형태: ${payType || "월급"}
2. 임금구성항목, 계산방법 및 지급방법은 회사의 급여규정 및 정산 내역에 따라 매월 정기적으로 지급한다.
3. 연장·야간·휴일근로 시에는 근로기준법에 따라 통상임금의 50% 이상을 가산하여 지급한다.

제6조 (상여금 및 제수당)
상여금, 식대, 교통비 등 제수당은 회사 취업규칙 및 수당템플릿에서 정한 바에 따라 지급한다.

제7조 (연차유급휴가)
연차유급휴가는 근로기준법 제60조에 따라 부여하며, 발생·사용 및 미사용수당 지급은 회사 휴가규정에 따른다.

제8조 (사회보험)
국민연금, 건강보험, 고용보험, 산재보험 등 4대 사회보험은 관계 법령이 정하는 바에 따라 적용한다.

제9조 (안전보건)
을은 생산공장 내 안전수칙, 위생관리기준(HACCP 등) 및 회사가 실시하는 안전교육(TBM)을 성실히 준수하여야 한다.

제10조 (근로계약서 교부)
갑은 본 계약서를 을에게 교부하며, 을은 전자적 방법(전자서명)으로 이를 확인 및 수령한다.

제11조 (성실이행의무)
갑과 을은 각자 근로계약, 취업규칙 및 단체협약을 지키고 성실하게 이행하여야 한다.

제12조 (기타)
본 계약에 정하지 않은 사항은 근로기준법 및 회사 취업규칙에 따른다.

위와 같이 근로계약을 체결하고 그 내용을 확인하기 위하여 아래에 서명(전자서명)한다.`;
}
